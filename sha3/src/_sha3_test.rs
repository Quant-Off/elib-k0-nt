    // 증분 update가 일괄 입력과 동일한 결과를 내는지 검증 (버퍼 경계 횡단 포함)
    #[test]
    fn incremental_update_equivalence() {
        let mut m = [0u8; 300];
        for (j, b) in m.iter_mut().enumerate() {
            *b = (j * 7 + 5) as u8;
        }

        let oneshot = sha3::<SHA3_256>(&m);
        for &chunk in &[1usize, 7, 64, 135, 136, 137, 299] {
            let mut h = SHA3_256::new();
            for piece in m.chunks(chunk) {
                h.update(piece);
            }
            assert_eq!(
                h.finalize().as_bytes(),
                oneshot.as_bytes(),
                "SHA3-256 청크 {chunk} 분할 결과 불일치"
            );
        }

        let mut xof_oneshot = [0u8; 99];
        shake::<SHAKE256>(&m, &mut xof_oneshot);
        for &chunk in &[1usize, 7, 135, 136, 137] {
            let mut x = SHAKE256::new();
            for piece in m.chunks(chunk) {
                x.update(piece);
            }
            let mut out = [0u8; 99];
            x.finalize_into(&mut out);
            assert_eq!(out, xof_oneshot, "SHAKE256 청크 {chunk} 분할 결과 불일치");
        }
    }

    // Drop 후 스펀지 상태와 입력 버퍼가 소거되는지 검증
    #[test]
    fn test_keccak_state_zeroize_on_drop() {
        let mut storage: MaybeUninit<KeccakState> = MaybeUninit::uninit();

        unsafe {
            storage.write(KeccakState::new(1088, 0x06));
            storage.assume_init_mut().update(&[0xA5u8; 200]);

            let state_ptr = storage.assume_init_ref().state.expose().as_ptr() as *const u8;
            let state_len = core::mem::size_of::<[u64; 25]>();
            let buf_ptr = storage.assume_init_ref().buffer.expose().as_ptr();

            let pre_state = core::slice::from_raw_parts(state_ptr, state_len);
            assert!(
                pre_state.iter().any(|&b| b != 0),
                "state 가 비어 있음 — 테스트 전제 위반"
            );
            let pre_buf = core::slice::from_raw_parts(buf_ptr, MAX_RATE_BYTES);
            assert!(
                pre_buf.iter().any(|&b| b != 0),
                "buffer 가 비어 있음 — 테스트 전제 위반"
            );

            storage.assume_init_drop();

            let post_state = core::slice::from_raw_parts(state_ptr, state_len);
            assert!(
                post_state.iter().all(|&b| b == 0),
                "KeccakState state 가 Drop 후 소거되지 않음"
            );
            let post_buf = core::slice::from_raw_parts(buf_ptr, MAX_RATE_BYTES);
            assert!(
                post_buf.iter().all(|&b| b == 0),
                "KeccakState buffer 가 Drop 후 소거되지 않음"
            );
        }
    }

    // Drop 후 다이제스트 바이트가 소거되는지 검증
    #[test]
    fn test_digest_zeroize_on_drop() {
        let mut storage: MaybeUninit<Digest> = MaybeUninit::uninit();

        unsafe {
            storage.write(sha3::<SHA3_256>(b"zeroize on drop"));
            let ptr = storage.assume_init_ref().bytes.expose().as_ptr();

            let pre = core::slice::from_raw_parts(ptr, 64);
            assert!(
                pre.iter().any(|&b| b != 0),
                "다이제스트가 비어 있음 — 테스트 전제 위반"
            );

            storage.assume_init_drop();

            let post = core::slice::from_raw_parts(ptr, 64);
            assert!(
                post.iter().all(|&b| b == 0),
                "Digest 가 Drop 후 소거되지 않음"
            );
        }
    }
